#ifndef _TYPEID_HPP_
#define _TYPEID_HPP_

#include <type_traits>

template<class T, class U=
    typename std::remove_cv<
        typename std::remove_pointer<
            typename std::remove_reference<
                typename std::remove_extent<T>::type
                >::type
            >::type
        >::type
    >
struct remove_all : remove_all<U> {};
template<class T> struct remove_all<T, T> { typedef T type; };

namespace Gearless
{
    using TypeId = unsigned long;

    class TypeIdGenBase
    {
        protected:
            static TypeId GenTypeId()
            {
                static TypeId nextId = 0;
                return nextId++;
            }
    };

    template<typename T>
    class TypeIdGen : public TypeIdGenBase
    {
        public:
            static TypeId GetTypeId()
            {
                static TypeId tId = TypeIdGenBase::GenTypeId();
                return tId;
            }
    };

    template<typename T>
    TypeId GetTypeId()
    {
        return TypeIdGen<typename remove_all<T>::type>::GetTypeId();
    }
}

#endif // ! _TYPEID_HPP_
